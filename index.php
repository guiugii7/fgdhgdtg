<?php
// Входные числа
$num1 = 10;
$num2 = 25;
$num3 = 7;

// Нахождение наибольшего числа
$largest = max($num1, $num2, $num3);

// Нахождение наименьшего числа
$smallest = min($num1, $num2, $num3);

// Вывод результатов
echo "Наибольшее число: " . $largest . "<br>";
echo "Наименьшее число: " . $smallest;
?>
